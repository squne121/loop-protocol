def _extract_pr_file_paths(item: dict[str, Any]) -> frozenset[str]:
    """Extract file paths from a PR's `files` field (gh pr list --json files)."""
    files = item.get("files", [])
    if not isinstance(files, list):
        return frozenset()
    paths: list[str] = []
    for f in files:
        if isinstance(f, dict):
            path = f.get("path") or f.get("filename") or ""
        elif isinstance(f, str):
            path = f
        else:
            path = ""
        path = path.replace("\\", "/").rstrip("/")
        if path:
            paths.append(path)
    return frozenset(paths)


def _paths_share_prefix(a: frozenset[str], b: frozenset[str]) -> bool:
    """Return True if any path in *a* is a prefix of a path in *b* or vice versa."""
    for pa in a:
        for pb in b:
            short, long = (pa, pb) if len(pa) <= len(pb) else (pb, pa)
            if long.startswith(short.rstrip("/") + "/") or long == short:
                return True
    return False


def _prefix_overlap_pairs(a: frozenset[str], b: frozenset[str]) -> list[str]:
    """Return normalised "parent -> child" path pairs for prefix overlaps.

    Used so that prefix-overlap candidates record the structural relationship in
    scope_context.anchor_paths instead of leaving it empty (which previously caused
    conflict_type to collapse to "none" — see #550 Blocker 4).
    """
    pairs: list[str] = []
    for pa in a:
        for pb in b:
            short, long = (pa, pb) if len(pa) <= len(pb) else (pb, pa)
            if long != short and long.startswith(short.rstrip("/") + "/"):
                pairs.append(f"{short} -> {long}")
    return sorted(set(pairs))


def _non_mergeable_reasons(item: dict[str, Any], kind: str) -> list[str]:
    """Collect reasons why a candidate may not be directly mergeable."""
    reasons: list[str] = []
    state = str(item.get("state", "")).lower()
    state_reason = str(item.get("stateReason", "") or "").lower()
    if state == "closed":
        if state_reason == "not_planned":
            reasons.append("closed_not_planned")
        elif state_reason == "completed":
            reasons.append("closed_completed")
        else:
            reasons.append(f"closed({state_reason or 'unknown'})")
    return reasons


def _build_candidates(
    current: dict[str, Any] | None,
    items: list[dict[str, Any]],
    kind: str,
) -> list[dict[str, Any]]:
    """Build candidate entries for a list of items (issues or PRs)."""
    if current is None:
        return []

    current_number = _extract_field(current, "number", default=None)
    current_dedupe_key = _extract_dedupe_key(current)
    current_allowed_paths = _extract_allowed_paths(current)
    current_parent = _extract_parent_issue(current)
    current_skill_family = _extract_skill_family(current)
    current_failure_mode = _extract_failure_mode_marker(current)

    candidates: list[dict[str, Any]] = []

    for item in items:
        item_number = _extract_field(item, "number", default=None)
        if item_number == current_number and kind == "issue":
            continue  # skip the current issue itself

        signals: list[str] = []
        matched_paths: list[str] = []
        prefix_overlap_paths: list[str] = []

        # Signal: shared_dedupe_key
        item_dedupe_key = _extract_dedupe_key(item)
        if current_dedupe_key and item_dedupe_key and current_dedupe_key == item_dedupe_key:
            signals.append(SIGNAL_SHARED_DEDUPE_KEY)

        # Signal: allowed-path overlap (three levels — B3)
        # For PRs, use the `files` field as primary path signal
        if kind == "pr":
            item_allowed_paths = _extract_pr_file_paths(item) or _extract_allowed_paths(item)
        else:
            item_allowed_paths = _extract_allowed_paths(item)

        if current_allowed_paths and item_allowed_paths:
            overlap = current_allowed_paths & item_allowed_paths
            if overlap:
                matched_paths = sorted(overlap)
                if current_allowed_paths == item_allowed_paths:
                    signals.append(SIGNAL_EXACT_ALLOWED_PATH_OVERLAP)
                else:
                    signals.append(SIGNAL_ALLOWED_PATH_INTERSECTION)
            elif _paths_share_prefix(current_allowed_paths, item_allowed_paths):
                signals.append(SIGNAL_ALLOWED_PATH_PREFIX_OVERLAP)
                # Record the parent -> child relationship so scope_context does not
                # collapse to conflict_type: none (#550 Blocker 4).
                prefix_overlap_paths = _prefix_overlap_pairs(
                    current_allowed_paths, item_allowed_paths
                )

        # Signal: same_parent_issue
        item_parent = _extract_parent_issue(item)
        if (
            current_parent
            and item_parent
            and current_parent.strip() == item_parent.strip()
        ):
            signals.append(SIGNAL_SAME_PARENT_ISSUE)

        # Signal: same_skill_family
        item_skill_family = _extract_skill_family(item)
        if (
            current_skill_family
            and item_skill_family
            and current_skill_family == item_skill_family
        ):
            signals.append(SIGNAL_SAME_SKILL_FAMILY)

        # Signal: same_failure_mode_marker
        item_failure_mode = _extract_failure_mode_marker(item)
        if (
            current_failure_mode
            and item_failure_mode
            and current_failure_mode == item_failure_mode
        ):
            signals.append(SIGNAL_SAME_FAILURE_MODE_MARKER)

        if not signals:
            continue  # no relationship detected

        confidence = _determine_confidence(signals)

        # Build scope_context (structural position + domain classification + conflict type).
        # Pass the resolved Allowed Paths (item_allowed_paths uses PR `files` for PRs) so
        # scope_context anchors on the same path set used for signal detection.
        scope_context = _build_scope_context(
            current,
            item,
            matched_paths,
            current_allowed_paths,
            item_allowed_paths,
            prefix_overlap_paths,
        )

        action = _suggested_action(item, signals, confidence, scope_context)

        # ordering_constraint: separate from suggested_action (temporal ordering concern)
        ordering_constraint = _determine_ordering_constraint(current, item, signals, confidence)

        # dedupe_key for the candidate itself
        candidate_dedupe_key = item_dedupe_key or f"{kind}-{item_number}"

        # Security evidence (B4) — kept for audit/backward-compat, not used for escalation
        sec_evidence = _security_match_evidence(item)

        # Non-mergeable reasons (closed issues/PRs from --state all)
        nmr = _non_mergeable_reasons(item, kind)

        entry: dict[str, Any] = {
            "kind": kind,
            "number": item_number,
            "title": _extract_field(item, "title", default=None),
            "url": _extract_field(item, "url", default=None),
            "state": _extract_field(item, "state", default=None),
            "confidence": confidence,
            "dedupe_key": candidate_dedupe_key,
            "signals": signals,
            "matched_paths": matched_paths,
            "suggested_action": action,
            "scope_context": scope_context,
            "ordering_constraint": ordering_constraint,
        }
        if sec_evidence:
            entry["security_match_evidence"] = sec_evidence
        if nmr:
            entry["non_mergeable_reasons"] = nmr

        candidates.append(entry)

    return candidates


_CURRENT_ISSUE_NOT_FOUND = "current_issue_not_found"


def run(
    issues_json_path: str,
    prs_json_path: str,
    current_issue_number: int | None = None,
    repo: str = "",
    invocation_id: str | None = None,
    script_path: Path | None = None,
) -> tuple[dict[str, Any], int]:
    """Main analysis function.

    Returns (ISSUE_SCOPE_ROLLUP_PLAN_V2 dict, exit_code).

    exit_code == 2 when current_issue_number is specified but not found in
    issues_json (B1: fallback to first issue is prohibited).
    exit_code == 0 otherwise.

    invocation_id: UUID4 string for this invocation. If None, a new uuid4 is generated.
    script_path: Path to this script file (used for script_file_sha256). Defaults to
                 the actual script file location (__file__).
    """
    warnings: list[str] = []
    exit_code = 0

    # Resolve invocation_id (generate if not provided)
    if invocation_id is None:
        invocation_id = str(uuid.uuid4())

    # Resolve script_path (default to this file)
    if script_path is None:
        script_path = Path(__file__).resolve()

    try:
        issues, issues_raw = _load_json(issues_json_path)
    except (json.JSONDecodeError, ValueError) as exc:
        issues = []
        issues_raw = ""
        warnings.append(f"issues_json parse error: {exc}")

    try:
        prs, prs_raw = _load_json(prs_json_path)
    except (json.JSONDecodeError, ValueError) as exc:
        prs = []
        prs_raw = ""
        warnings.append(f"prs_json parse error: {exc}")

    body_sha256 = _sha256_of_inputs(issues_raw, prs_raw)

    # Identify the "current" issue — B1: NO fallback to first issue
    current_issue: dict[str, Any] | None = None
    if current_issue_number is not None:
        for iss in issues:
            if _extract_field(iss, "number", default=None) == current_issue_number:
                current_issue = iss
                break
        if current_issue is None:
            # B1: do NOT fall back to issues[0]; report partial completeness + exit 2
            warnings.append(_CURRENT_ISSUE_NOT_FOUND)
            exit_code = 2
    elif issues:
        current_issue = issues[0]

    completeness = "partial" if (warnings or exit_code != 0) else "full"

    if exit_code == 2:
        # B1: no candidates when current issue is not found
        all_candidates: list[dict[str, Any]] = []
    else:
        issue_candidates = _build_candidates(current_issue, issues, "issue")
        pr_candidates = _build_candidates(current_issue, prs, "pr")
        all_candidates = issue_candidates + pr_candidates

    plan_without_self_validation: dict[str, Any] = {
        "schema_version": SCHEMA_VERSION,
        "repo": repo,
        "generated_at": datetime.now(timezone.utc).isoformat().replace("+00:00", "Z"),
        "source": SOURCE,
        "body_sha256": body_sha256,
        "input": {
            "completeness": completeness,
            "warnings": warnings,
        },
        "candidates": all_candidates,
    }

    # Build self_validation block (payload_sha256 covers plan without self_validation)
    self_validation = _build_self_validation(
        plan_without_self_validation,
        invocation_id,
        script_path,
    )

    plan: dict[str, Any] = {**plan_without_self_validation, "self_validation": self_validation}

    return plan, exit_code


